
//for loop

let nums = [30, 40, 50, 60];

for (let i = 0; i < nums.length; i++) {
  console.log(nums[i]);
}

//while loop

let num = 10;
while (num < 20) {
  console.log(num);
  num = num + 1;
}


//switch case

let firstName = "asif";

switch (firstName) {
  case "hasan":
    console.log("I am hasan");
    break;
  case "asif":
    console.log("I am asif");
    break;
  default:
    console.log("Not found");
}